package com.lfa.oop.util;

/**
 *
 * @author satye
 */
public class CalculatorHelper {
    
    public double add(double x, double y){
        return x+y;
    }
    
    public double sub(double x, double y){
        return x-y;
    }
    
    public double mul(double x, double y){
        return x*y;
    }
    
    public double div(double x, double y){
        return x/y;
    }
}
